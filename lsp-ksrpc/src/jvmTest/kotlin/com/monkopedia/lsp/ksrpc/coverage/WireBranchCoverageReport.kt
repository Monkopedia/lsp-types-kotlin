package com.monkopedia.lsp.ksrpc.coverage

import java.io.File

/**
 * Builds and writes the Markdown union-branch coverage report (#74).
 *
 * Sibling to the method-coverage report at `wire-coverage.md`. Lists every
 * sealed interface/class in `com.monkopedia.lsp.*` along with each permitted
 * subclass marked yes/no (covered / not covered) based on whether the
 * integration suite walked an instance of that subclass.
 *
 * The report is intentionally Markdown so the artifact is human-readable in
 * CI; the totals line is parseable by the verification test, which fails the
 * build on regression.
 */
internal object WireBranchCoverageReport {

    /** Snapshot of one suite run: the full union surface + observed branches. */
    data class CoverageState(
        /** Map from union FQN → set of `<UnionFqn>.<Subclass>` branch identifiers. */
        val unions: Map<String, Set<String>>,
        /** Set of `<UnionFqn>.<Subclass>` strings actually observed over the wire. */
        val observed: Set<String>
    ) {
        val totalBranches: Int get() = unions.values.sumOf { it.size }
        val coveredBranches: Int get() = unions.values.sumOf { branches ->
            branches.count { it in observed }
        }
    }

    /** Snapshot the recorder + introspect the union surface. */
    fun snapshot(): CoverageState = CoverageState(
        unions = WireBranchRecorder.introspectUnions(),
        observed = WireBranchRecorder.observedBranches
    )

    /** Render the Markdown report for the given coverage state. */
    fun render(state: CoverageState): String = buildString {
        val totalBr = state.totalBranches
        val coveredBr = state.coveredBranches
        appendLine("# LSP Wire-Branch Coverage Report")
        appendLine()
        appendLine(
            "Generated by `WireBranchCoverageReportTest` (#74). The recorder " +
                "introspects every sealed interface / class in `com.monkopedia.lsp.*` " +
                "and records every subclass an instance of which was walked through " +
                "the conformance fixtures during the JVM integration suite."
        )
        appendLine()
        appendLine("## Totals")
        appendLine()
        appendLine("- branches: $coveredBr/$totalBr (${pct(coveredBr, totalBr)})")
        appendLine("- unions fully covered: ${fullyCoveredUnions(state)}/${state.unions.size}")
        appendLine()
        appendLine("## Per-union coverage")
        appendLine()
        val sortedUnions = state.unions.keys.sorted()
        for (unionFqn in sortedUnions) {
            val branches = state.unions[unionFqn].orEmpty().sorted()
            val covered = branches.count { it in state.observed }
            appendLine("### `${unionFqn.substringAfterLast('.')}` ($covered/${branches.size})")
            appendLine()
            appendLine("FQN: `$unionFqn`")
            appendLine()
            appendLine("| Subclass | Covered |")
            appendLine("| --- | --- |")
            for (branchFqn in branches) {
                val subclass = branchFqn.removePrefix("$unionFqn.")
                val mark = if (branchFqn in state.observed) "yes" else "no"
                appendLine("| `$subclass` | $mark |")
            }
            appendLine()
        }
    }

    /** Write the report to [outFile], creating parent directories if needed. */
    fun writeTo(outFile: File, state: CoverageState = snapshot()) {
        outFile.parentFile?.mkdirs()
        outFile.writeText(render(state))
    }

    private fun fullyCoveredUnions(state: CoverageState): Int =
        state.unions.values.count { branches ->
            branches.isNotEmpty() && branches.all { it in state.observed }
        }

    private fun pct(num: Int, denom: Int): String =
        if (denom == 0) "n/a" else "%d%%".format(num * 100 / denom)
}
