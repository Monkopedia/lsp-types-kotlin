package com.monkopedia.lsp.ksrpc.coverage

import java.io.File

/**
 * Builds and writes the Markdown wire-coverage report (#66).
 *
 * Produces two tables — server-side and client-side — grouped by LSP namespace
 * prefix (`textDocument/`, `workspace/`, `window/`, `notebookDocument/`,
 * `callHierarchy/`, `typeHierarchy/`, `$/`, `<other>`). Each row marks the
 * method covered (yes) or uncovered (no). A footer line shows the totals and
 * percentage.
 *
 * The report file is intentionally Markdown so the artifact is human-readable
 * in CI, and the totals/percentages line is parseable by a follow-up
 * verification step that fails the build on regression.
 */
internal object WireCoverageReport {

    /** Namespaces, in display order. */
    private val NAMESPACES: List<Pair<String, (String) -> Boolean>> = listOf(
        "textDocument/" to { it.startsWith("textDocument/") },
        "workspace/" to { it.startsWith("workspace/") },
        "workspaceSymbol/" to { it.startsWith("workspaceSymbol/") },
        "window/" to { it.startsWith("window/") },
        "notebookDocument/" to { it.startsWith("notebookDocument/") },
        "callHierarchy/" to { it.startsWith("callHierarchy/") },
        "typeHierarchy/" to { it.startsWith("typeHierarchy/") },
        "client/" to { it.startsWith("client/") },
        "telemetry/" to { it.startsWith("telemetry/") },
        "inlayHint/" to { it.startsWith("inlayHint/") },
        "completionItem/" to { it.startsWith("completionItem/") },
        "codeAction/" to { it.startsWith("codeAction/") },
        "codeLens/" to { it.startsWith("codeLens/") },
        "documentLink/" to { it.startsWith("documentLink/") },
        "\$/" to { it.startsWith("\$/") },
        // catch-all: lifecycle methods (initialize, shutdown, exit, initialized)
        "<other>" to { _ -> true }
    )

    /** Render the Markdown report for the given coverage state. */
    fun render(state: CoverageState): String = buildString {
        appendLine("# LSP Wire-Method Coverage Report")
        appendLine()
        appendLine(
            "Generated by `WireCoverageReportTest` (#66). The recorder introspects " +
                "the LSP method-name constants on `LanguageServer.Companion` / " +
                "`LanguageClient.Companion` and records every wire method the conformance " +
                "fixtures see during the JVM integration suite."
        )
        appendLine()
        appendLine("## Server-side methods")
        appendLine()
        appendSection(this, state.serverSurface, state.serverCalled)
        appendLine()
        appendLine("## Client-side methods")
        appendLine()
        appendSection(this, state.clientSurface, state.clientCalled)
        appendLine()
        appendLine("## Totals")
        appendLine()
        val sCov = state.serverCalled.size
        val sTot = state.serverSurface.size
        val cCov = state.clientCalled.size
        val cTot = state.clientSurface.size
        val sPct = pct(sCov, sTot)
        val cPct = pct(cCov, cTot)
        appendLine("- server: $sCov/$sTot ($sPct)")
        appendLine("- client: $cCov/$cTot ($cPct)")
    }

    private fun appendSection(out: StringBuilder, surface: Set<String>, called: Set<String>) {
        val remaining = surface.toMutableSet()
        for ((label, predicate) in NAMESPACES) {
            val group = remaining.filter(predicate).sorted()
            if (group.isEmpty()) continue
            remaining.removeAll(group.toSet())
            val covered = group.count { it in called }
            val groupSize = group.size
            out.appendLine("### `$label` ($covered/$groupSize)")
            out.appendLine()
            out.appendLine("| Method | Covered |")
            out.appendLine("| --- | --- |")
            for (m in group) {
                val mark = if (m in called) "yes" else "no"
                out.appendLine("| `$m` | $mark |")
            }
            out.appendLine()
        }
    }

    private fun pct(num: Int, denom: Int): String =
        if (denom == 0) "n/a" else "%d%%".format(num * 100 / denom)

    /**
     * Snapshot of one suite run: the full surface (introspected) plus the set
     * of methods actually exercised over the wire.
     */
    data class CoverageState(
        val serverSurface: Set<String>,
        val clientSurface: Set<String>,
        val serverCalled: Set<String>,
        val clientCalled: Set<String>
    )

    /** Snapshot the recorder + introspect both surfaces. */
    fun snapshot(): CoverageState = CoverageState(
        serverSurface = WireMethodRecorder.introspectServerSurface(),
        clientSurface = WireMethodRecorder.introspectClientSurface(),
        serverCalled = WireMethodRecorder.serverCalled,
        clientCalled = WireMethodRecorder.clientCalled
    )

    /** Write the report to [outFile], creating parent directories if needed. */
    fun writeTo(outFile: File, state: CoverageState = snapshot()) {
        outFile.parentFile?.mkdirs()
        outFile.writeText(render(state))
    }
}
